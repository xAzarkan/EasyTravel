package com.example.easytravel_sofiane_azarkan.Model;

public class PopularCity {
    private double averageTemperature;
    private String name;
    private String averageIcon;
    private String averageDescription;

    public double getAverageTemperature() {
        return averageTemperature;
    }

    public void setAverageTemperature(double averageTemperature) {
        this.averageTemperature = averageTemperature;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAverageIcon() {
        return averageIcon;
    }

    public void setAverageIcon(String averageIcon) {
        this.averageIcon = averageIcon;
    }

    public String getAverageDescription() {
        return averageDescription;
    }

    public void setAverageDescription(String averageDescription) {
        this.averageDescription = averageDescription;
    }
}
